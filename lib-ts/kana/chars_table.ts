import { CharFlags, CharKind } from './chars_types'

export const TABLE: { [key: string]: [CharKind, CharFlags] } = {}

function register(kind: CharKind, flags: CharFlags, ...keys: string[]) {
	for (const k of keys) {
		const kc = k.normalize()
		const kd = k.normalize('NFD')
		TABLE[k] = [kind, flags]
		if (kc !== k) {
			TABLE[kc] = [kind, flags]
		}
		if (kd !== kc) {
			TABLE[kd] = [kind, flags]
		}
	}
}

